package day8;

public class HelloModulo {
    public static void main(String[] args) {
        System.out.println(56 / 24);
        System.out.println(56 % 24); // остаток от деления
        int a = 5;
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);
        int b = 3;
        a %=b;
        System.out.println(a);
    }
}
